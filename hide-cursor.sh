#!/bin/bash
# Create a 16x16 transparent cursor to hide the pointer completely
BLANK="/tmp/blank.xbm"
cat > "$BLANK" << 'XBM'
#define blank_width 16
#define blank_height 16
static unsigned char blank_bits[] = {
   0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
   0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
   0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
   0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00};
XBM
xsetroot -cursor "$BLANK" "$BLANK"
